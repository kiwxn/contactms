    $stmt = $pdo->prepare('INSERT INTO Users (username, email, password_hash) VALUES (?, ?, ?)');
    try {
        $stmt->execute([$username, $email, $password]);
        echo 'User registered successfully!';
    } catch (PDOException $e) {
        echo 'Error: ' . $e->getMessage();
    }
